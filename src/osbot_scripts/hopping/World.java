package osbot_scripts.hopping;

public class World {
	
	private int world;
	
	private int amount;

	/**
	 * @return the world
	 */
	public int getWorld() {
		return world;
	}

	/**
	 * @param world the world to set
	 */
	public void setWorld(int world) {
		this.world = world;
	}

	/**
	 * @return the amount
	 */
	public int getAmount() {
		return amount;
	}

	/**
	 * @param amount the amount to set
	 */
	public void setAmount(int amount) {
		this.amount = amount;
	}

}
